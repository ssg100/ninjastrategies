// This namespace holds all strategies and is required. Do not change it.
namespace NinjaTrader.Strategy
{
    /// <summary>
    /// Trading the V and inverse-V pivot
    /// </summary>
    [Description("Trading the V and inverse-V pivot")]
    public class ZZTradePivots : Strategy
    {
        #region Variables
        // Wizard generated variables
        private double profitTarget = 6.000; // Default setting for ProfitTarget
        private double stopLoss = 2.000; // Default setting for StopLoss
        private double trailingStop = 1; // Default setting for TrailingStop
        // User defined variables (add any user defined variables below)
        #endregion

        /// <summary>
        /// This method is used to configure the strategy and is called once before any strategy method is called.
        /// </summary>
        protected override void Initialize()
        {
            SetStopLoss("long", CalculationMode.Percent, StopLoss, false);

            CalculateOnBarClose = true;
        }

        /// <summary>
        /// Called on each bar update event (incoming tick)
        /// </summary>
        protected override void OnBarUpdate()
        {
            // Condition set 1
            if (Close[0] > High[1]
                && Position.MarketPosition == MarketPosition.Flat)
            {
                EnterLong(100, "long");
            }

            // Condition set 2
            if (Close[0] < Low[1]
                && Position.MarketPosition == MarketPosition.Long)
            {
                ExitLong("exitlong", "long");
            }

            // Condition set 3
            if (Close[0] < Low[1]
                && Position.MarketPosition == MarketPosition.Flat)
            {
                EnterShort(100, "short");
            }

            // Condition set 4
            if (Close[0] > High[1]
                && Position.MarketPosition == MarketPosition.Short)
            {
                ExitShort("exitshort", "short");
            }
        }

        #region Properties
        [Description("")]
        [GridCategory("Parameters")]
        public double ProfitTarget
        {
            get { return profitTarget; }
            set { profitTarget = Math.Max(1, value); }
        }

        [Description("")]
        [GridCategory("Parameters")]
        public double StopLoss
        {
            get { return stopLoss; }
            set { stopLoss = Math.Max(1, value); }
        }

        [Description("")]
        [GridCategory("Parameters")]
        public double TrailingStop
        {
            get { return trailingStop; }
            set { trailingStop = Math.Max(1, value); }
        }
        #endregion
    }
}

