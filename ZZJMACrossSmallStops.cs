namespace NinjaTrader.Strategy
{
    /// <summary>
    /// Cut losses fast and let profit run
    /// </summary>
    [Description("Cut losses fast and let profit run")]
    public class ZZJMACrossSmallStops : Strategy
    {
        #region Variables
        // Wizard generated variables
        private int shortMAlen = 10; // Default setting for ShortMAlen
        private int longMAlen = 50; // Default setting for LongMAlen
        private int entryPullbackTicks = 1; // Default setting for EntryPullbackTicks
        private double aTRFactor = 1; // Default setting for ATRFactor
        // User defined variables (add any user defined variables below)
        #endregion

        /// <summary>
        /// This method is used to configure the strategy and is called once before any strategy method is called.
        /// </summary>
        protected override void Initialize()
        {
            Add(JurikJMA(0, ShortMAlen));
            Add(JurikJMA(0, LongMAlen));
            SetStopLoss("long", CalculationMode.Ticks, 10, false);

            CalculateOnBarClose = true;
        }

        /// <summary>
        /// Called on each bar update event (incoming tick)
        /// </summary>
        protected override void OnBarUpdate()
        {
            // Condition set 1
            if (CrossAbove(JurikJMA(0, ShortMAlen), JurikJMA(0, LongMAlen), 1)
                && Position.MarketPosition == MarketPosition.Flat)
            {
                EnterLong(DefaultQuantity, "long");
            }

            // Condition set 2
            if (CrossBelow(JurikJMA(0, ShortMAlen), JurikJMA(0, LongMAlen), 1)
                && Position.MarketPosition == MarketPosition.Long)
            {
                ExitLong("exitlong", "long");
            }
        }

        #region Properties
        [Description("")]
        [GridCategory("Parameters")]
        public int ShortMAlen
        {
            get { return shortMAlen; }
            set { shortMAlen = Math.Max(1, value); }
        }

        [Description("")]
        [GridCategory("Parameters")]
        public int LongMAlen
        {
            get { return longMAlen; }
            set { longMAlen = Math.Max(1, value); }
        }

        [Description("")]
        [GridCategory("Parameters")]
        public int EntryPullbackTicks
        {
            get { return entryPullbackTicks; }
            set { entryPullbackTicks = Math.Max(1, value); }
        }

        [Description("")]
        [GridCategory("Parameters")]
        public double ATRFactor
        {
            get { return aTRFactor; }
            set { aTRFactor = Math.Max(1, value); }
        }
        #endregion
    }
}

